
public class directorRow {

	public String title;
	public int year;
	public int length;
	public Double rating;
	public String directorName;

	directorRow(String title, int year, int length, Double rating, String directorName) {
		this.year = year;
		this.title = title;
		this.length = length;
		this.rating = rating;
		this.directorName = directorName;
	}
}
